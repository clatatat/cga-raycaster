/* RCTEX.C - Compiled-in 8x8 textures for CGA text-mode raycaster */
/* Each texture is 8x8 = 64 unsigned short values (VRAM words).   */
/* Each word = (attribute << 8) | ASCII_char, matching CGA VRAM.  */
/*                                                                 */
/* Design constraints:                                             */
/* - Must be distinguishable on 4-shade monochrome LCD (Compaq LTE)*/
/* - Uses IBM Extended ASCII graphics characters (codes 176-223)   */
/* - Pattern structure matters more than color for mono screens    */
/*                                                                 */
/* IBM Extended Graphics Characters used:                          */
/*   0xB0 = ░ (light shade)    0xB1 = ▒ (medium shade)            */
/*   0xB2 = ▓ (dark shade)     0xDB = █ (full block)              */
/*   0xB3 = │ (thin vertical)  0xBA = ║ (double vertical)         */
/*   0xC4 = ─ (thin horiz)     0xCD = ═ (double horiz)            */
/*   0xD9 = ┘  0xBF = ┐  0xDA = ┌  0xC0 = └                      */
/*   0xC5 = ┼ (cross)          0xFE = ■ (small block)             */
/*   0x20 = space                                                  */
/*   color values  */
/*   0 = black, 1 = blue, 2 = green, 3 = cyan, 4 = red, 5 = magenta, 6 = brown, 7 = light gray */
/*   8 = dark gray, 9 = light blue, A = light green, B = light cyan, C = light red, D = light magenta, E = yellow, F = white */

